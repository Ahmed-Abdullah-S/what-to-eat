export const getFilteredMeals = (filters: {
  priceLevel?: string;
  cuisine?: string;
  dietaryType?: string;
}) => {
  return meals.filter(meal => {
    if (filters.priceLevel && meal.priceLevel !== filters.priceLevel) return false;
    if (filters.cuisine && meal.cuisine !== filters.cuisine) return false;
    if (filters.dietaryType && meal.dietaryType !== filters.dietaryType) return false;
    return true;
  });
};

export const getRandomMeal = (filters?: {
  priceLevel?: string;
  cuisine?: string;
  dietaryType?: string;
}) => {
  const filteredMeals = filters ? getFilteredMeals(filters) : meals;
  if (filteredMeals.length === 0) return null;
  const randomIndex = Math.floor(Math.random() * filteredMeals.length);
  return filteredMeals[randomIndex];
}; 